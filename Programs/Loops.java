public class Loops {
    public static void main(String[]args){
        int a=0;
        int b=0;

        for(int i=0; i<5; i++){
            System.out.println("*");
        }
        while(a<5){
            System.out.println("$");
            a++;

        }
        do{
            System.out.println("@");
            b++; 
        }
        while(b<5);
    }
    
}
